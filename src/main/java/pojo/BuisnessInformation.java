package pojo;

public class BuisnessInformation {
	
	private String lead_id;
	private String occupation_type;
	private String business_vintage;
	private String industry_master_id;
	private String action;
	
	public String getLead_id() {
		return lead_id;
	}
	public void setLead_id(String lead_id) {
		this.lead_id = lead_id;
	}
	public String getOccupation_type() {
		return occupation_type;
	}
	public void setOccupation_type(String occupation_type) {
		this.occupation_type = occupation_type;
	}
	public String getBusiness_vintage() {
		return business_vintage;
	}
	public void setBusiness_vintage(String business_vintage) {
		this.business_vintage = business_vintage;
	}
	public String getIndustry_master_id() {
		return industry_master_id;
	}
	public void setIndustry_master_id(String industry_master_id) {
		this.industry_master_id = industry_master_id;
	}
	public String getAction() {
		return action;
	}
	public void setAction(String action) {
		this.action = action;
	}
	
	
	
	

}
